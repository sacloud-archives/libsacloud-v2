package main

import (
	"log"
	"path/filepath"

	"github.com/sacloud/libsacloud-v2/internal/define"
	"github.com/sacloud/libsacloud-v2/internal/tools"
)

const destination = "sacloud/fake/zz_resources.go"

func init() {
	log.SetFlags(0)
	log.SetPrefix("gen-api-meta: ")
}

func main() {
	outputPath := destination
	tools.WriteFileWithTemplate(&tools.TemplateConfig{
		OutputPath: filepath.Join(tools.ProjectRootPath(), outputPath),
		Template:   tmpl,
		Parameter:  define.Resources,
	})
	log.Printf("generated: %s\n", outputPath)
}

const tmpl = `// generated by 'github.com/sacloud/libsacloud/internal/tools/gen-api-meta'; DO NOT EDIT

package fake

const (
{{ range . -}}
	// Resource{{.TypeName}} is resource key of fake store
	Resource{{.TypeName}} = "{{.TypeName}}"
{{ end -}}
)
`
