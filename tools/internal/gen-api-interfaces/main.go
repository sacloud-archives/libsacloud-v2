package main

import (
	"log"
	"path/filepath"

	"github.com/sacloud/libsacloud-v2/define"
	"github.com/sacloud/libsacloud-v2/tools/internal"
)

const destination = "sacloud/zz_apis.go"

func init() {
	log.SetFlags(0)
	log.SetPrefix("gen-api-interfaces: ")
}

func main() {
	internal.WriteFileWithTemplate(&internal.TemplateConfig{
		OutputPath: filepath.Join(internal.ProjectRootPath(), destination),
		Template:   tmpl,
		Parameter:  define.Resources,
	})
	log.Printf("generated: %s\n", filepath.Join(destination))
}

const tmpl = `// generated by 'github.com/sacloud/libsacloud/tools/internal/gen-api-interfaces'; DO NOT EDIT

package sacloud

import (
{{- range .ImportStatements -}}
	{{ . }}
{{- end -}}
)

{{ range . }}
// {{ .Name }}API is interface for operate {{ .Name }} resource
type {{ .Name }}API interface {
{{ range .Operations }}
	{{ .Name }}(ctx context.Context{{ range .Arguments }}, {{ .Name }} {{ .TypeName }}{{ end }}) ({{ range .Results }}{{ .TypeName }}, {{ end }})
{{- end -}}
}
{{ end }}
`
